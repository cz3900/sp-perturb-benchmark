md("## 0. What's new in this refactor (read this first)\n"
   "\n"
   "**1. Two-layer adapters.** An *input* adapter (`SaundersAdapter`) normalises a raw `.h5mu` "
   "slice into one `StandardData`. An *output* adapter (`models.scgen_model.ScgenSeedModel`) "
   "normalises every model's native output. Models never touch each other's formats.\n"
   "\n"
   "**2. Sample-level aggregate control** (`reference_aggregate.aggregate_control` -> "
   "`harness._control_reference_aggregate`). The reference 'unperturbed' state is now a "
   "per-cell-type aggregate over CONTROL cells only — no nearest-neighbour matched-control pairing "
   "in feature space, so no matched-control leakage. The SAME aggregate-control reference now "
   "drives EVERY comparison: the seed baseline (`seed_ref`) and the niche no-effect baseline "
   "(`e_null` = the average same-type control cell's neighbourhood) both come from "
   "`reference_aggregate.control_reference_centers`. `run_benchmark` uses this by default.\n"
   "\n"
   "**3. `eval_X` — one unified scoring space.** PCC-delta is not cross-space robust, so the "
   "prediction, the observed cells, and the delta baseline must all be compared in the SAME space. "
   "For scGEN the natural space is its log-norm training space: we pass `eval_X=lognorm_X` (the "
   "`build_lognorm_X` matrix) into `fill_2x2`, which slices the observed/reference seed cells into "
   "that same matrix so scGEN's seed is scored fairly.\n"
   "\n"
   "**4. MC-spatial quadrant x dimension stratification.** MC-spatial flags, per guide, whether a "
   "real **Self** (X) and/or **Niche** (Y) effect exists, giving four quadrants: **Both**, "
   "**X-Only (Self)**, **Y-Only (Niche)**, **Inert**. We score **seed only where there is Self "
   "signal** (X-Only/Both), **niche only where there is Niche signal** (Y-Only/Both), and use the "
   "**Inert** quadrant as a rigorous **negative control** (a good model should predict ~no effect "
   "there). This avoids diluting a niche score over perturbations that have no niche signal.")

