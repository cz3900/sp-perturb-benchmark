   "\n"
   "**We report everything as `E-distance`: LOWER = better, with NO subtraction.** Energy distance "
   "is always **>= 0**, so we report the (mean) energy distance to the observed perturbed cells "
   "directly — no `log` wrapper (the niche E is small, well under 1, because it is a neighbourhood "
   "aggregate, but it is never negative). From `res['seed'][p]['e_samples']` and "
   "`res['compare'][p]['e']`:\n"
