     "                    gcn_kwargs={'hidden': 64, 'epochs': 30})\n"
     "\n"
     "# Everything below is reported as E-distance: LOWER = better, NO subtraction. Energy\n"
     "# distance is always >= 0, so we report the raw (mean) E with no log wrapper.\n"
     "# null (no-effect aggregate-control niche) and oracle (leakage-free ceiling) are\n"
     "# REFERENCE values shown alongside, never subtracted. PCC-delta is supplementary.\n"
     "def _meanE(v):\n"
     "    \"\"\"Mean per-repeat E (linear, >=0); nan when there are no samples.\"\"\"\n"
     "    return float(np.nanmean(v)) if np.size(v) else float('nan')\n"
     "\n"
     "rows = []\n"
     "for p in EVAL:\n"
     "    s = res['seed'][p]; c = res['compare'][p]\n"
     "    es = s.get('e_samples', {})\n"
     "    rows.append(dict(\n"
     "        perturbation=p,\n"
     "        quadrant=QUADRANT_OF.get(p, mcj.UNKNOWN),\n"
     "        seed_E=_meanE(es.get('model', [])),                 # seed (TrivialSeed) E\n"
     "        seed_null_E=_meanE(es.get('null', [])),             # seed control-seed reference\n"
     "        niche_E=float(c['e']['model+learned']),             # niche (deployable) E\n"
     "        niche_null_E=float(c['e']['null']),                 # niche no-effect reference\n"
     "        niche_oracle_E=(float(c['e']['oracle']) if 'oracle' in c['e'] else float('nan')),  # niche ceiling ref\n"
